        The number of vehicles increases daily rapidly, so the problem of traffic congestion increases day by day, so this reduces the space available for parking.

        Therefore, there is an urgent need to create a vehicle refinery, whose management system is fully automated, making it a smart park, given the availability of IoT evolution and the availability of technologies that can be used to build a smart parking.<br><br><b></b>

        
        The smart parking entrance will be made up of an electronic portal that will open in the event of empty spaces in the parking lot, and if there are no empty spaces in the parking lot, the gate will remain closed, the process of checking empty spaces will be through a telephone application through which the user will get a result from examining the sensor network of the empty places in the parking.<br><br>
        
        The sensor network is one of the main components that will be in the smart parking lot, where there will be a sensor located everywhere dedicated in the parking lot, to check if the place is empty or not, after which the results of the sensor scan will be sent to the phone app by knowing where the empty places are in the parking.<br><br>
        
        After the vehicle enters the empty place, it will be known where it stood by taking a picture of the vehicle number by a camera installed in the position and sending it to another team whose task will be to process and improve the images and then pass the vehicle number to the smart parking control system.<br><br><b></b>
        
        The parking control system will calculate the hours when the vehicle will remain parked in the designated location, calculate the cost and send it to the user via the phone app and display the available payment methods.
        
        Many electronic elements and the formation of integrated circuits will be used to work within the situation fully computerized without the need for human intervention, and the user will be able to check if there are vacancies in the smart position from anywhere, so building smart positions will work to solve the suffering of finding a smart position and saving the user time and effort in this matter.
